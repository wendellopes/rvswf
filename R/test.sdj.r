#-------------------------------------------------------------------------------
# Derivative of Spherical Bessel Functions
#-------------------------------------------------------------------------------
tst.sbdj<-function(x,n){
   a<-n/(2*n+1)
   b<-(n+1)/(2*n+1)
	return(a*tst.sbjn(x,n-1)-b*tst.sbjn(x,n+1))
}
