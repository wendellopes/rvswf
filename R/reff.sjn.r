#-------------------------------------------------------------------------------
# Spherical Bessel Functions
#-------------------------------------------------------------------------------
reff.sjn<-function(x,n){
   if(x==0){
      if(n==0){
         u<-1
      }else{
         u<-0
      }
   }else{
	   u<-sqrt(pi/2)*besselJ(x,n+.5)/sqrt(x)
   }
   return(u)
}
