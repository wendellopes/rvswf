# Derivative of Riccati Bessel Functions
#-------------------------------------------------------------------------------
reff.rdj<-function(x,n){
   a<-n/(2*n+1)
   b<-(n+1)/(2*n+1)
	return(b*reff.rjn(x,n-1)-a*reff.rjn(x,n+1))
}
